def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--list-databases", action="store_true")
    args = parser.parse_args()
    session = login()
    if args.list_databases:
        list_databases(session)
        return
    database_id = get_database_id()
    collection_id = get_or_create_collection(session)
    card_ids = {q.name: make_question(session, collection_id, database_id, q) for q in QUESTIONS}
    for spec in DASHBOARDS:
        dashboard_id = get_or_create_dashboard(session, collection_id, spec)
        update_dashboard_layout(session, dashboard_id, spec.cards, card_ids)
        print(f"Dashboard ready: {spec.name} -> {METABASE_URL.rstrip('/')}/dashboard/{dashboard_id}")
    print("Done.")
if __name__ == "__main__":
    main()

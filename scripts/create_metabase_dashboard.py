#!/usr/bin/env python3
"""Create THC Analytics dashboards in Metabase v0.61.x.
Requirements:
    pip install requests python-dotenv
Usage:
    1) Copy .env.example to .env and fill credentials.
    2) List databases:
       python scripts\\create_metabase_dashboard.py --list-databases
    3) Set METABASE_DATABASE_ID in .env.
    4) Create/update dashboards:
       python scripts\\create_metabase_dashboard.py
Notes:
    Metabase v0.61 does not support the old POST /api/dashboard/:id/cards endpoint.
    This script updates dashboard cards through PUT /api/dashboard/:id.
"""
from __future__ import annotations
import argparse
import os
import sys
from dataclasses import dataclass
from typing import Any
import requests
try:
    from dotenv import load_dotenv
except Exception:
    load_dotenv = None
METABASE_URL = os.environ.get("METABASE_URL", "http://localhost:3000")
COLLECTION_NAME = "THC Analytics"
TIMEOUT = 60
@dataclass
class Question:
    name: str
    sql: str
    display: str
    visualization_settings: dict[str, Any]
@dataclass
class DashboardCard:
    question_name: str
    x: int
    y: int
    w: int
    h: int
@dataclass
class DashboardSpec:
    name: str
    description: str
    cards: list[DashboardCard]
def getenv_required(name: str) -> str:
    value = os.environ.get(name)
    if not value:
        raise SystemExit(f"Missing required environment variable: {name}")
    return value
def request(session: requests.Session, method: str, path: str, **kwargs: Any) -> Any:
    url = f"{METABASE_URL.rstrip('/')}{path}"
    response = session.request(method, url, timeout=TIMEOUT, **kwargs)
    if response.status_code >= 400:
        print(f"\nHTTP {response.status_code} {method} {url}", file=sys.stderr)
        try:
            print(response.json(), file=sys.stderr)
        except Exception:
            print(response.text, file=sys.stderr)
        response.raise_for_status()
    if response.text:
        try:
            return response.json()
        except Exception:
            return response.text
    return None
def login() -> requests.Session:
    if load_dotenv:
        load_dotenv()
    email = getenv_required("METABASE_EMAIL")
    password = getenv_required("METABASE_PASSWORD")
    session = requests.Session()
    payload = {"username": email, "password": password}
    data = request(session, "POST", "/api/session", json=payload)
    token = data["id"]
    session.headers.update({"X-Metabase-Session": token})
    return session
def list_databases(session: requests.Session) -> None:
    data = request(session, "GET", "/api/database")
    for db in data.get("data", []):
        print(f'{db["id"]}: {db["name"]} ({db.get("engine")})')
def get_database_id() -> int:
    value = getenv_required("METABASE_DATABASE_ID")
    try:
        return int(value)
    except ValueError as exc:
        raise SystemExit("METABASE_DATABASE_ID must be an integer") from exc
def get_or_create_collection(session: requests.Session) -> int:
    collections = request(session, "GET", "/api/collection")
    for item in collections:
        if item.get("name") == COLLECTION_NAME:
            return int(item["id"])
    created = request(session, "POST", "/api/collection", json={"name": COLLECTION_NAME, "description": "theHunter Classic analytics dashboards"})
    return int(created["id"])
def native_query(database_id: int, sql: str) -> dict[str, Any]:
    return {
        "database": database_id,
        "type": "native",
        "native": {"query": sql, "template-tags": {}},
    }
def list_cards_in_collection(session: requests.Session, collection_id: int) -> list[dict[str, Any]]:
    try:
        cards = request(session, "GET", f"/api/card?f=all&collection={collection_id}")
        if isinstance(cards, list):
            return cards
    except requests.HTTPError:
        pass
    search = request(session, "GET", "/api/search", params={"models": "card", "collection": collection_id})
    return [item for item in search.get("data", []) if item.get("model") == "card"]
def make_question(session: requests.Session, collection_id: int, database_id: int, q: Question) -> int:
    existing = list_cards_in_collection(session, collection_id)
    for card in existing:
        if card.get("name") == q.name:
            card_id = int(card["id"])
            payload = {
                "name": q.name,
                "description": "Generated by THC dashboard automation",
                "dataset_query": native_query(database_id, q.sql),
                "display": q.display,
                "visualization_settings": q.visualization_settings,
                "collection_id": collection_id,
            }
            try:
                request(session, "PUT", f"/api/card/{card_id}", json=payload)
                print(f"Question updated: {q.name} -> {card_id}")
            except requests.HTTPError:
                print(f"Question exists: {q.name} -> {card_id}")
            return card_id
    payload = {
        "name": q.name,
        "description": "Generated by THC dashboard automation",
        "dataset_query": native_query(database_id, q.sql),
        "display": q.display,
        "visualization_settings": q.visualization_settings,
        "collection_id": collection_id,
        "result_metadata": None,
    }
    created = request(session, "POST", "/api/card", json=payload)
    print(f"Question created: {q.name} -> {created['id']}")
    return int(created["id"])
def get_or_create_dashboard(session: requests.Session, collection_id: int, spec: DashboardSpec) -> int:
    search = request(session, "GET", "/api/search", params={"q": spec.name})
    for item in search.get("data", []):
        if item.get("model") == "dashboard" and item.get("name") == spec.name:
            dashboard_id = int(item["id"])
            request(session, "PUT", f"/api/dashboard/{dashboard_id}", json={"name": spec.name, "description": spec.description, "collection_id": collection_id})
            print(f"Dashboard exists: {spec.name} -> {dashboard_id}")
            return dashboard_id
    payload = {"name": spec.name, "description": spec.description, "collection_id": collection_id}
    created = request(session, "POST", "/api/dashboard", json=payload)
    print(f"Dashboard created: {spec.name} -> {created['id']}")
    return int(created["id"])
def make_dashcard(dashboard_id: int, card_id: int, x: int, y: int, w: int, h: int, temp_id: int) -> dict[str, Any]:
    return {
        "id": temp_id,
        "dashboard_id": dashboard_id,
        "card_id": card_id,
        "card": {"id": card_id},
        "row": y,
        "col": x,
        "size_x": w,
        "size_y": h,
        "parameter_mappings": [],
        "visualization_settings": {},
        "series": [],
    }
def update_dashboard_layout(session: requests.Session, dashboard_id: int, cards: list[DashboardCard], card_ids: dict[str, int]) -> None:
    dashcards = [
        make_dashcard(dashboard_id, card_ids[item.question_name], item.x, item.y, item.w, item.h, -index - 1)
        for index, item in enumerate(cards)
    ]
    payload_variants = [
        {"dashcards": dashcards},
        {"cards": dashcards},
        {"ordered_cards": dashcards},
    ]
    last_error: Exception | None = None
    for payload in payload_variants:
        try:
            request(session, "PUT", f"/api/dashboard/{dashboard_id}", json=payload)
            return
        except requests.HTTPError as exc:
            last_error = exc
    raise RuntimeError(f"Could not update dashboard {dashboard_id} layout") from last_error
def settings_table(cols: list[str] | None = None) -> dict[str, Any]:
    return {"table.columns": cols or []}
def settings_bar(x: str, y: str, color: str = "#7fb069") -> dict[str, Any]:
    return {"graph.dimensions": [x], "graph.metrics": [y], "graph.colors": [color]}
def settings_line(x: str, ys: list[str]) -> dict[str, Any]:
    return {"graph.dimensions": [x], "graph.metrics": ys, "graph.colors": ["#d97706", "#3b82f6", "#7fb069"]}
